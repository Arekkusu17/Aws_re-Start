print("Python has three numeric types: int, float and complex")

"""
Integer Value
"""

myValue=1

print(myValue)

print(type(myValue))

print(f"{myValue} is of the data type {type(myValue)}")


"""
Float Value
"""

myValue=3.14

print(myValue)

print(type(myValue))

print(f"{myValue} is of the data type {type(myValue)}")


"""
Complex Value
"""

myValue=5j

print(myValue)

print(type(myValue))

print(f"{myValue} is of the data type {type(myValue)}")

"""
Booleans Values
"""

myValue=True

print(myValue)

print(type(myValue))

print(f"{myValue} is of the data type {type(myValue)}")

myValue=False

print(myValue)

print(type(myValue))

print(f"{myValue} is of the data type {type(myValue)}")

